'''
Exercício 2 - Tuplas
Criar uma tupla com um único item.
'''

# without comma
single_tuple = ('Hello' )
print(type(single_tuple))
# Output class 'str'
print(single_tuple)
# Output Hello

# with comma
single_tuple1 = ('Hello' ,)
# output class 'tuple'
print(type(single_tuple1))
# Output ('Hello' ,)
print(single_tuple1)